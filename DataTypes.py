#Data Types 
# Variable = A container for a value (String , integer , float , boolean)
#            A variable behave as if it was the value it contain

# Strings
first_name = "Bro"
print(f"Hello {first_name}") # f is the f stream , it is formatting


#Integer
age = 25

print(f"You are {age} year old")

# float
price = 10.29
print(f"The price is {price}")

# Boolean

is_student = True

print(f"Are you student: {is_student}")